package com.ktds.curtain.replyDislike.vo;

public class ReplyDislikeVO {
	
	private int replyDislikeId;
	private String dislikeDate;
	private int replyId;
	private String email;
	
	public int getReplyDislikeId() {
		return replyDislikeId;
	}
	public void setReplyDislikeId(int replyDislikeId) {
		this.replyDislikeId = replyDislikeId;
	}
	public String getDislikeDate() {
		return dislikeDate;
	}
	public void setDislikeDate(String dislikeDate) {
		this.dislikeDate = dislikeDate;
	}
	public int getReplyId() {
		return replyId;
	}
	public void setReplyId(int replyId) {
		this.replyId = replyId;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
}
