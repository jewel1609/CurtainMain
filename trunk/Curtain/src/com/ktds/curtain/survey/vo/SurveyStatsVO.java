package com.ktds.curtain.survey.vo;

public class SurveyStatsVO extends SurveyVO {
	
	private int surveyStatsId;
	private int firstStats;
	private int secondStats;
	private int thirdStats;
	private int fourthStats;
	public int getSurveyStatsId() {
		return surveyStatsId;
	}
	public void setSurveyStatsId(int surveyStatsId) {
		this.surveyStatsId = surveyStatsId;
	}
	public int getFirstStats() {
		return firstStats;
	}
	public void setFirstStats(int firstStats) {
		this.firstStats = firstStats;
	}
	public int getSecondStats() {
		return secondStats;
	}
	public void setSecondStats(int secondStats) {
		this.secondStats = secondStats;
	}
	public int getThirdStats() {
		return thirdStats;
	}
	public void setThirdStats(int thirdStats) {
		this.thirdStats = thirdStats;
	}
	public int getFourthStats() {
		return fourthStats;
	}
	public void setFourthStats(int fourthStats) {
		this.fourthStats = fourthStats;
	}
	
	
	
	
}
